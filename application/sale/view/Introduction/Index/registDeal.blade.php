            <p class="text-indent"> 第三十一条 售后争议处理系指交易完结后买卖双方提起交易保障要求，就相关要求对交易赔偿作出处理的基本程序与标准。</p>
			<p class="text-indent">第三十二条符合《消费者保障服务协议》约定条件的买家在交易成功后提起的未收到货、商品描述不符、商品存在质量问题、卖家未履行售后服务或特色服务的保障要求的，参照本规范售中争议处理相应条款进行认定，《消费者保障服务协议》中有特别规定的除外。</p>
