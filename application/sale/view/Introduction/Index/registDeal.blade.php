			 <h4>第一节 一般规定</h4>
			<p class="text-indent">第六条 售中争议处理系指交易完结(系指系统显示“交易成功”或“交易关闭”时的状态)前买卖双方提起交易保障要求，根据相关要求对交易款项归属作出处理的基本程序与标准。</p>
			<p class="text-indent">第七条 本章规定事项为售中争议处理的一般规定，本规范第四章特殊交易争议处理规范有特殊要求的，依照第四章的规定处理。</p>

			 <h4>第二节 交易标的规范</h4>
			<p class="text-indent">第八条 卖家交付给买家的商品应当符合法律法规的相关规定，且所售商品不得违反《规则》中关于发布违禁信息、出售假冒商品、发布禁售信息、滥发信息、发布未经准入商品、假冒材质成份、出售未经报关进口商品、发布非约定商品等条款的相关规定。</p>
			<p class="text-indent">第九条 卖家应当对其所售商品进行如实描述，即应当在商品描述页面、店铺页面、聊天工具等所有提供的渠道中，对商品的基本属性、成色、瑕疵等必须说明的信息进行真实、完整的描述。</p>
			<p class="text-indent">第十条 卖家应当对其所售商品质量承担保证责任，即保证其交付给买家的商品在合理期限内可以正常使用，包括商品不存在危及人身财产安全的不合理危险、具备商品应当具备的使用性能、符合商品或其包装上注明采用的标准等。
			卖家应当保证其提供的商品或者服务符合保障人身、财产安全的要求。对可能危及人身、财产安全的商品和服务，应当向买家作出真实的说明和明确的警示，并说明和标明正确使用商品或者接受服务的方法以及防止危害发生的方法。</p>
			<p class="text-indent">第十一条 商品属于“三包”(系指法律法规规定的三包或所在地适用的三包，以下简称“三包规定”)范围内的，买家要求卖家履行换货或维修义务的，卖家应当按照买家的要求提供相应服务。</p>

			<h4>第三节 交易行为规范</h4>
			<p class="text-indent">第十二条 买卖双方应当进行真实交易，不进行以增加信用或销量为目的的虚假交易。</p>
			<p class="text-indent">第十三条 发货规范</p>
			<p class="text-indent">（一）卖家应当在买家付款后的72小时内或与买家约定的时间内发货，但有特殊规定的除外。买家申请退款时卖家尚未发货的，卖家应当征得买家同意后再发货。卖家逾期发货，或者未经买家同意在买家申请退款后发货，卖家应当追回已经发出的商品，如商品已到目的地的，买家可拒绝签收并不承担任何费用，但买家同意签收并确认收货的除外。</p>
			<p class="text-indent">（二）卖家应当按照订单约定的收货地址发货。</p>
			<p class="text-indent">（三）除非买卖双方另有约定，卖家应当负责将货物送达到买家收货地址。
			商品需要买家到指定地点提取的，应当在发货前告知买家并征得买家同意。
			卖家违反前述规定的，买家有权拒绝签收商品。</p>
			<p class="text-indent">第十四条 签收规范</p>
			<p class="text-indent">（一）买家应当在订单中向卖家提供准确的收货地址和收货人信息。买家需要变更订单中的收货地址或收货人信息的，应当征得卖家的明确同意。</p>
			<p class="text-indent">（二）买家在提供收货人信息时，可以选择本人或者他人作为收货人。买家选择他人作为收货人，该收货人违反本节约定义务的，由买家承担相应责任。</p>
			<p class="text-indent">（三）因买家填写的收货地址和(或)收货人信息不准确，或者未经卖家同意要求变更收货地址或收货人信息，导致卖家发货后无法送达的，运费由买家承担。</p>
			<p class="text-indent">（四）买家应当确保填写的收货地址和收货人信息准确并完整，若买家只填写了收货地址，但没有填写收货人或填写的收货人信息不特定，商品在收货地址被签收的，该签收视为买家本人签收。</p>
			<p class="text-indent">（五）卖家按照约定发货后，收货人有收货的义务。
			收货人无正当理由拒绝签收商品的，运费由买家承担，本规范另有说明的除外。
			收货人拒绝签收商品后，卖家应当及时联系承运人取回商品。因卖家怠于取回商品所产生的额外运费、保管费等费用由卖家承担。</p>
			<p class="text-indent">（六）收货人可以本人签收商品或委托他人代为签收商品，被委托人的签收视为收货人本人签收。</p>
			<p class="text-indent">（七）收货人签收商品时，应当对商品进行验收。
			涉及商品表面一致的事项，收货人应当在签收商品时进行验收。
			对于不能在签收商品时验收的事项，收货人应当在确认收货前进行验收。
			本条所称“表面一致”，是指凭肉眼即可判断所收到的商品表面状况良好且与网上描述相符，包括货物的形状、大小、数量、重量等性状。</p>
			<p class="text-indent">（八）收货人签收商品时发现表面不一致的情形，有权拒绝签收商品。</p>
			对于需要先签收再打开包装查看的商品，收货人应当要求承运人当场监督并打开包装查看，如发现表面不一致，应当在签收单（收货人联和承运人联）上备注详细情况并让承运人签字确认或者直接退回商品。</p>
			<p class="text-indent">（九）收货人签收商品后，商品毁损、灭失的风险由卖家转移给买家。</p>
			<p class="text-indent">第十五条退换货规范</p>
			<p class="text-indent">（一）买卖双方达成退货或换货协议，或做出退货退款的处理结果后，卖家应当在收到处理结果后的24小时内或者与买家约定的时间内提供退货地址。卖家逾期未提供退货地址的，以其在系统内填写的“默认退货地址”作为退货地址。</p>
			<p class="text-indent">（二）交易由买家承担退货运费时，如卖家的退货地址和商品页面中“运费”或“配送”板块显示的发货地不一致，买家退货至上述发货地之外运费超出部分由卖家承担。</p> 
			<p class="text-indent">（三）卖家提供的退货地址错误导致买家操作退回商品后无法送达的，卖家承担因此产生的运费。</p>
			<p class="text-indent">（四）买家根据协议约定或做出的处理结果操作退货时，应当使用与卖家发货时相同的运输方式发货。除非得到卖家的明确同意，买家不得使用到付方式支付运费。退货后，卖家有收货的义务。</p>
			<p class="text-indent">（五）买家有权自收到商品之日起七日内退货（退货的商品应当完好），且无需说明理由，除买家定作的、鲜活易腐的、在线下载或者买家拆封的音像制品、计算机软件等数字化商品、交付的报纸、期刊。其他根据商品性质并经买家在购买时确认不宜退货的商品，也不适用无理由退货。</p>
			<p class="text-indent">第十六条 买卖双方应当在交易前对商品情况、交易过程进行详细、清晰明确的约定。</p>

			<h4>第四节 物流规范</h4>
			<p class="text-indent">第十七条 卖家应当使用有揽收和签收凭证的运输方式发货。买家要求使用没有揽收和签收凭证的运输方式(以下简称“平信方式”)发货的，卖家应当在发货前以聊天的方式明确告知买家该方式存在的风险。</p>
			<p class="text-indent">第十八条 买卖双方指定了特定承运人的，卖家应当委托该特定承运人运送商品。</p>
			卖家违反前款约定的，买家有权拒绝签收商品，由此产生的运费及货物风险由卖家承担。</p>
			<p class="text-indent">第十八条 买卖双方指定了特定承运人的，卖家应当委托该特定承运人运送商品。
			第十九条 经买家明确同意，卖家可以使用到付方式委托承运人发货。</p>
			未经买家明确同意，卖家使用到付方式委托承运人发货的，买家有权拒绝签收商品；买家签收商品的，到付运费超出约定运费的部分由卖家承担。
			本规范所称到付方式系指在委托承运人时不支付运费，而由收货人在收货时向承运人支付运费的运输方式。</p>
			<p class="text-indent">第二十条 运费</p>
			<p class="text-indent">（一）卖家应当对运费的承担和组成做出清晰、准确的描述。
			商品描述中对运费做出两个以上的不同描述，或者实际发生的运费与商品描述的运费不一致的，卖家应当通过聊天工具向买家进行说明，并取得买家的同意。
			卖家违反前款约定的，以有利于买家的描述支付运费。</p>
			<p class="text-indent">（二）运费由买家承担的，卖家应当按照实际发生的金额向买家收取运费。</p>
			<p class="text-indent">（三）商品在换货或维修过程中需要寄送且未约定运费承担方式的，因此产生的运费由卖家承担。</p>
			<p class="text-indent">（四）买家以七天无理由形式退货或拒签的，来回运费均由买家承担。若商品提供包邮服务，买家仅承担退货运费。卖家和买家另有约定的，按照约定。</p>

			<h4>第五节 争议处理</h4>
			<p class="text-indent">第二十一条 卖家出售的商品违反法律法规规定，或为依据《规则》中出售假冒商品、假冒材质成份相关规定不得出售的商品，交易做退款处理；若为发布违禁信息、发布禁售信息相关规定不得出售的商品，交易视实际情形做退款或退货退款处理。卖家交付买家的商品存在大量劣质或货不对板情形的，经综合判断，有理由认为卖家无履行合同之意图的，则视为卖家拒绝履约，交易支持退款买家，商品由卖家自行和买家协商处置。</p>
			<p class="text-indent">第二十二条 商品存在质量问题或与网上描述不符的，交易做退货退款处理。商品并非假冒商品且没有质量问题的，交易做完成处理。因商品存在危及人身财产安全的不合理危险，导致买家损失的，由买卖双方另行协商或通过其他途径解决。</p>
			<p class="text-indent">第二十三条 卖家交付给买家的交易凭证虚假，卖家不同意重新开具且买家拒绝保留商品的，交易做退货退款处理，运费由卖家承担。</p>
			<p class="text-indent">第二十四条 买卖双方进行虚假交易的，在交易未完成前（售中），交易做退款处理；在交易完成后（售后），不予受理。</p>
			<p class="text-indent">第二十五条 卖家违反发货规范，导致买家未收到货、拒绝签收商品或者签收后退回商品的，交易做退款处理，运费由卖家承担。
			卖家提供凭证证明买家已收到货，买家未提出其他异议的，交易做完成处理。</p>
			<p class="text-indent">第二十六条 买家依据签收规范拒绝签收商品或者退回商品的，交易做退款处理，运费由卖家承担。
			涉及表面一致的事项，买家提供有效凭证证明签收时已存在表面不一致情形的，交易做退款或退货退款处理；买家无法有效举证的，交易做打款处理。
			对于不能在签收商品时验收的事项产生争议的，适用本规范第二十二条的规定进行处理。</p>
			<p class="text-indent">第二十七条 买家或卖家违反退换货规范的，做如下处理：</p>
			<p class="text-indent">（一）卖家未在规定时间内提供退货地址，或者提供退货地址错误导致买家无法退货或操作退回商品后无法送达的，或者买家根据协议约定操作退货后，卖家无正当理由拒绝签收商品的，交易做退款处理，退货运费由卖家承担。卖家需要取回商品的，应当与买家另行协商或通过其他途径解决。</p>
			<p class="text-indent">（二）买家逾期未根据协议约定或规定时间操作退货的，交易做完成处理。</p>
			<p class="text-indent">（三）商品在退货过程中损毁的，商品退回买家或买家无理由拒签后，交易做完成处理。</p>
			<p class="text-indent">（四）买卖双方达成退货退款协议或做出退货退款处理的交易，商品退回至卖家退货地址后，有权退款给买家。</p>
			<p class="text-indent">（五）买卖双方达成换货协议的交易，卖家收到买家退回的商品后逾期未再次发货的，有权退款给买家。</p>
			<p class="text-indent">（六）就退货退款处理的跨境交易而言，因卖家原因导致买家无法退货的，交易做退款处理，货物由双方自行协商处置或通过其他途径解决。</p>
			<p class="text-indent">第二十八条 买卖双方约定不清，根据本规范无法确定争议的责任归属的，交易做退货退款处理，发货运费由卖家承担，退货运费由买家承担。因约定不清导致的其他损失由买卖双方共同承担，承担比例由根据具体情况判断。</p>
			<p class="text-indent">第二十九条 买家或卖家违反物流规范的，做如下处理：</p>
			<p class="text-indent">（一）未向买家提示风险而使用平信方式发货的，交易做退款处理。</p>
			<p class="text-indent">（二）运费由买家承担情况下，买家支付的运费高于实际发生的运费的，超出部分做退款处理；约定运费低于实际发生的运费的，依照本规范第一百零二条规定执行。</p>
			<p class="text-indent">（三）卖家违反物流规范导致买家未收到货、拒绝签收商品或在签收后退回商品的，交易做退款处理，运费由卖家承担。</p>
			<p class="text-indent">第三十条运费承担</p>
			<p class="text-indent">（一）交易中的运费争议，根据“谁过错，谁承担”的原则处理，但买卖双方协商一致的除外。本规范已经做出明确规定的，按照相应的规定处理。</p>
			<p class="text-indent">（二）买卖双方达成退款协议，但未就运费进行约定的，由卖家承担与其发货相同货运方式的运费。</p>
			<p class="text-indent">（三）处理争议期间，卖家同意退货或换货，但就运费的承担提出明确异议的，买家应当先行退货，卖家签收商品后，由根据本规范对运费承担做出处理。</p>
          </div>
        </dd>
      </dl>

