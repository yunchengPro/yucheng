           <div class="am-accordion-content">
	        <h4> 第一节 申请和受理</h4>
			<p class="text-indent">第一百条买家申请退款后，买卖双方可以选择自行协商、要求介入或通过司法途径等方式解决存在的争议。
			任一方要求介入的，即有权根据本规范对争议进行处理。</p>
			<p class="text-indent">第一百○一条买卖双方向申请争议处理，必须符合以下条件：</p>
			<p class="text-indent">（一）就售中争议而言，因买家未收到商品、商品表面不一致、收到的商品与描述不符、商品存在质量问题产生争议的，买家应当在付款后，确认收货前或在《规则》超时打款的时限内提出退款申请。</p>
			<p class="text-indent">（二）就售后争议而言，符合以下条件的争议处理，买家可以提出退款申请：</p>
			<p class="text-indent">1、买家对其所购买的“机动车、计算机、电视机、电冰箱、空调器、洗衣机或者装饰装修服务”主张存在质量问题或系假冒商品的，买家应当在交易成功后的六个月内提出售后申请。卖家应当按照本规范第八十五条和第八十六条的规定承担先行举证责任。</p>
			<p class="text-indent">2、除前款所述商品外，其余系假冒商品产生争议的，买家应当在交易成功后的九十天内（含节假日）提出退款申请，行政机关认定为假冒的，不受九十天限制。</p>
			<p class="text-indent">3、购买商品为虚拟物品的，因未收到货产生争议的，买家应当在交易成功后的十五天内提出退款申请，虚拟物品使用期限短于该期限的，买家应当在虚拟物品的使用期限内提出退款申请。</p>
			<p class="text-indent">4、因收到的商品与描述不符产生争议的，买家应当在交易成功后的十五天内提出退款申请。</p>
			<p class="text-indent">5、享受“三包规定”保障的商品产生的保障范围内的争议，买家应当在交易成功后的九十天内提出售后申请。</p>
			<p class="text-indent">6、类目对售后争议处理的受理有特殊规定的，依照类目的特殊规定。</p>
			<p class="text-indent">第一百○二条 对符合第九十一条的争议处理申请，予以受理；对下列申请，分别情形，予以处理：</p>
			<p class="text-indent">（一）买家超出规定时限提出退款申请的，不予受理。</p>
			<p class="text-indent">（二）卖家向买家提示使用平信方式的风险后，买家仍要求使用平信方式发货的，相应的争议不予处理。</p>
			<p class="text-indent">（三）运费由买家承担情况下，约定运费低于实际发生的运费的，不足部分由买卖双方自行协商或通过其他途径解决，不予处理。</p>
			<p class="text-indent">（四）货到付款交易产生的运费争议，不予处理。</p>
			<p class="text-indent">（五）买卖双方经自行协商达成退款协议并履行完毕，一方或双方反悔产生争议的，不予处理；买卖双方经自行协商达成退款协议但尚未履行，一方或双方反悔，要求介入的，按照本规范进行处理。</p>
			<p class="text-indent">（六）交易做不退货退款处理后，卖家需要取回商品的，应当联系买家自行协商或通过其他途径解决，不予处理。</p>
			<p class="text-indent">（七）买卖双方实际交易商品与订单显示商品不一致，因实际交易商品产生的争议，不予处理。</p>
			<p class="text-indent">（八）驳回买家退款要求后，买家再次申请的不予处理。</p>
			<p class="text-indent">（九）本规范有特别规定的其他情形，不予处理。</p>

			<h4>第二节 争议的处理</h4>
			<p class="text-indent">第一百○三条   处理争议期间，买卖双方应当按照系统的提示和(或）发送的短信、电话或邮件通知及时提供凭证。</p>
			<p class="text-indent"><p class="text-indent">第一百○四条收集到双方提供的凭证后，将在系统提示的时间内，按照本规范对相应争议做出处理；本规范没有明确规定的，由依其独立判断做出处理。
			任何一方无正当理由，未按照前款规定提供凭证的，有权按照实际收集到的凭证做出处理。</p>
			<p class="text-indent">第一百○五条按照其独立判断，根据不同情况，将对买卖双方的争议做以下类型的处理：</p>
			<p class="text-indent">（一）交易做退款处理的，交易款项支付给买家，货物由交易双方自行协商处理。</p>
			<p class="text-indent">（二）交易做退款处理的，交易款项支付给买家。</p>
			<p class="text-indent">（三）交易做退货退款处理的，在卖家签收退货后，交易款项支付给买家。</p>
			<p class="text-indent">（四）交易协商不退货部分退款处理的，交易款项分别支付给买家和卖家。</p>
			<p class="text-indent">（五）交易做打款处理的，交易款项支付给卖家。</p>
			<p class="text-indent">（六）买家退款理由不成立的，驳回买家退款要求。</p>


			<h4>第三节 处理中止</h4>
			<p class="text-indent">第一百○六条处理争议期间，有下列情形之一的，中止处理相应争议：</p>
			<p class="text-indent">（一）买卖双方一致要求自行协商处理争议；</p>
			<p class="text-indent">（二）任何一方通知要求通过司法机关解决争议或拟向公安机关报案。</p>
			<p class="text-indent">第一百○七条争议处理程序恢复</p>
			<p class="text-indent">（一）买卖双方一致要求自行协商处理争议的，应当在提出协商要求后的30天内自行操作交易款项，或向提供协商结果，由代为操作。逾期未操作交易款项也未告知协商结果的，有权根据本规范处理争议。</p>
			<p class="text-indent">（二）一方通知要求通过司法机关解决争议或拟向公安机关报案的，应当在通知后的7个工作日内向提供司法机关或公安机关的案件受理凭证。逾期未提供案件受理凭证的，有权恢复争议处理程序。</p>
			<p class="text-indent">（三）公安机关受理案件后的六个月内，未对交易案件给出调查结论或处理建议的，有权恢复争议处理程序。司法机关受理案件后，将等待案件的终局裁判结果。</p>
			
			<h4>第四节 执行</h4>
				<p class="text-indent">第一百○八条处理争议期间，买卖双方达成退款协议，但无法自行操作的，将根据双方达成的退款协议，通知支付宝公司操作相应的交易款项和(或)保证金。</p>
				<p class="text-indent">第一百○九条对争议做出处理后，有权通知支付宝公司按照处理结果将交易款项和（或）保证金的全部或部分支付给买家和(或)卖家，或由卖家(或买家)按照处理结果将相关款项支付给买家(或卖家)。</p>
				<p class="text-indent">第一百一十条在将交易款项全部支付给买家后，根据争议处理结果仍需卖家承担责任的，有权通知支付宝公司，自卖家根据《消费者保障服务协议》或《天猫服务协议》或《飞猪服务协议》缴纳的保证金或其他款项中划拨相应的钱款补偿支付给买家。
			交易款项或保证金支付后，根据处理结果仍需卖家(或买家)承担责任的，由买卖双方自行协商或通过其他途径解决。</p>
				<p class="text-indent">第一百一十一条处理争议期间，使用自有资金代替卖家先行退款给买家（亦称“先行垫付”）的，交易做出处理后，有权通知支付宝公司，将交易款项、保证金或卖家支付宝账户中与先行垫付金额相当的部分划扣至账户。</p>
				

			<h4>第五节 其他救济方式</h4>
				<p class="text-indent">第一百一十二条对争议做出处理并通知支付宝公司支付争议款项后，买家或(和)卖家对的处理有异议的，可向人民法院起诉。</p>
				<p class="text-indent">第一百一十三条争议通过司法途径解决的，将根据司法机关的生效法律文书，视情形通知支付宝公司操作相应的交易款项和相关保证金。</p>
          </div>
