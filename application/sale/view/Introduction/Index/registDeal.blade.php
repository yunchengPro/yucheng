       <dl class="am-accordion-item">
        <dt class="am-accordion-title">
          第四章 特殊商品/交易争议处理规范
        </dt>
        <dd class="am-accordion-bd am-collapse ">
          <!-- 规避 Collapase 处理有 padding 的折叠内容计算计算有误问题， 加一个容器 -->
          <div class="am-accordion-content">
           <h4> 第一节 当面交易处理规范</h4>
			 <p class="text-indent">第三十三条 买卖双方采用当面交付商品的方式进行交易的，应当自行承担因此产生的风险。</p>
			 <p class="text-indent">第三十四条 因买家未收到货产生的争议，交易做退款处理，但卖家可以证明买家自认收到商品的除外。</p>
			 <p class="text-indent">第三十五条 因商品表面不一致产生的争议，交易做退货处理，但买家可以证明卖家自认商品存在表面不一致的除外。</p>
			 <p class="text-indent">第三十六条 因商品描述不符、存在质量问题产生的争议，适用本规范第二十二条的规定进行处理。</p>

			 <h4>第二节 代购服务处理规范</h4>
			<p class="text-indent">第三十七条 本规范所称代购服务，是指卖家根据买家的委托，在海外及港澳台代为购买指定商品（该商品为非现货）的服务。不符合前述要求的，则该交易不视为代购服务交易，相应的争议适用第二章的规定处理。</p>
			<p class="text-indent">第三十八条 发布代购信息时，卖家应当按照商品发布规范对所代购的商品进行描述，并对卖家自身和厂家对代购商品的售后服务政策、代购涉及的代理费、运费等进行完整的描述。卖家所提供的售后服务政策低于厂家的，卖家应当向买家说明并取得买家的同意。
			卖家未对自身和厂家所提供的代购商品的售后服务政策进行说明，或者卖家所提供的售后服务政策低于厂家但未取得买家同意，导致商品无法享受售后服务的，交易做退货退款处理。
			若商品在退货过程中非因买家责任导致商品被境内外海关没收、扣押或采取其他强制措施的，相应风险由卖家承担。</p>
			<p class="text-indent">第三十九条 代购商品运输需要经过海关的，卖家应确保代购商品符合相关海关法律法规的规定。若代购商品违反法律法规的规定，导致商品被海关没收、扣押或采取其他强制措施的，相应的风险由卖家承担。
			卖家应对通关手续和可能发生的其他通关费用等事项进行描述。若通关手续中包含了境内法定税费的，则该费用由买家承担，双方另有约定的，从其约定。没有对通关手续和其他通关费用等事项进行描述的，由卖家负责办理通关手续并承担相应费用。因卖家怠于或拒绝办理通关手续，由此可能产生的通关滞货费用（如额外运费、相应仓储、保管费等）及商品灭失风险均由卖家承担。
			办理通关手续需要买家协助的，买家应当提供相应协助。因买家怠于或拒绝提供协助导致商品无法办理通关手续的，由此可能产生的通关滞货费用（如额外运费、相应仓储、保管费等）及商品灭失风险均由买家承担。</p>

			<p class="text-indent">第四十条 代购服务过程中，若买卖双方约定的发货时间未到，而卖家已产生实质代购行为的，则卖家有权拒绝买家未收到货的退款申请，但卖家明确表明不履行代购服务的除外。
			若卖家已依约履行发货义务，买家无正当理由拒绝签收商品的，相应风险由买家承担；但商品运输过程中因不可抗力或物流公司原因导致物流延迟或通关滞货的，自发货之日起六十日后，买家尚未收到商品，交易可退款处理，货物风险由卖家承担。</p>
			
			<h4>第三节 定金交易处理规范</h4>
			<p class="text-indent">第四十一条 需要买家预先支付部分款项的交易，买卖双方应当具体约定该部分款项的支付和返还条件。
			仅以“定金”、“订金”、“预付款”等字样进行描述，但未明确约定具体支付和返还条件的，相应争议按照第二章和第三章的规定处理。</p>

			<h4>第四节 定制交易处理规范</h4>
			<p class="text-indent">第四十二条 买卖双方进行定制商品交易的，应当对商品的交付时间、交付标准做明确的约定。
			未约定具体交付时间的，卖家应当在买家首次要求交付后的三天内发货。
			未约定交付标准的，适用本规范第二十八条的规定进行处理。</p>

			<p class="text-indent">第四十三条 买卖双方约定的商品交付时间届满前，买家无正当理由要求退款的，需与卖家协商一致。但卖家明确表明不履行定制合同的除外。</p>
			<p class="text-indent">第四十四条 买家无正当理由拒绝签收定制商品的，交易完成处理，买卖双方有约定的从其约定。</p>

			<h4>第五节 直邮海外交易处理规范</h4>
			<p class="text-indent">第四十五条 本规范所称直邮海外，系指买卖双方依据订单及相关约定，卖家通过国际物流运送方式，直接将商品送达至海外买家的交易行为。
			海外：系指港澳台地区以及中华人民共和国领域外的国家和地区。
			海外买家：系指收货地址位于海外的会员。
			<p class="text-indent">第四十六条 卖家需确保所售商品符合商品出境的相关法律法规，买家需确保所购商品符合商品入境的相关法律法规。若商品因违反出境相关法律法规导致被监管部门（如海关）没收、扣押或采取其他强制措施的，相应出境风险由卖家承担，若商品因违反入境相关法律法规导致被监管部门（如海关）没收、扣押或采取其他强制措施的，相应入境风险由买家承担。
			<p class="text-indent">第四十七条 通关手续中包含了入境地法定税费的，除双方约定外，该费用由买家承担。
			<p class="text-indent">第四十八条 办理通关出入境手续需要买家或卖家合理的协助配合，相对方应提供相关协助。若一方怠于或拒绝提供协助导致商品无法办理通关手续的，由此可能产生的通关滞货费用（如额外运费、相应仓储、保管费等）及商品灭失风险均由该责任方承担。
			<p class="text-indent">第四十九条 若发现商品存在破损、污渍等问题，买家需在收到商品的48小时内拍照向卖家或举证（物流显示妥投（签收）记录的次日零时起计48小时），逾期举证有权不予采纳。
			<p class="text-indent">第五十条 买家若主张少件，在外包装完好的前提下，可提供清晰完整的拆包视频，经认定有效后，将依据视频内容予以判定。
			无法提供或视频内容模糊难认的，有权根据物流系统内记录的重量及其他实际情况予以推算及认定。
			<p class="text-indent">第五十一条 若买家能提供物流公司有效证明上述第四十九条及第五十条所列主张事由，经核实有效的，则将根据证明所载之内容并结合实际情况作出判定。
			<p class="text-indent">第五十二条 卖家应确保按订单载明及双方约定的内容按时、按质、按量交付商品。因卖家责任产生退货的，当买家提供无法退货的凭证（如被海关扣押等）或客观上退货存障的，卖家应主动向买家提供协商处理的方案。协商不成的，有权根据买家合理诉求进行处理。
			<p class="text-indent">第五十三条 非卖家责任产生退货的，由买家承担退货风险。</p>

			<h4>第六节 生鲜类商品争议处理规范</h4>
			<p class="text-indent">第五十四条 本规范所称的生鲜类商品是指商品所在类目为“水产肉类/新鲜蔬果/熟食”下的商品。</p>
			<p class="text-indent">第五十五条 生鲜类商品，买家无正当理由拒绝签收的，导致生鲜类商品出现损伤、变质、腐烂死亡情形的，相关运费及商品相应风险由买家承担。买卖双方另有约定的，从其约定。</p>
			<p class="text-indent">第五十六条 生鲜类商品存在腐烂、重量短缺、大小不符三类情形的，分别作如下处置：</p>
			<p class="text-indent">（一）生鲜类商品存在大小不符或腐烂情形的，其数量占商品总量百分之三十以内的，按相应比例作部分退款；</p>
			<p class="text-indent">（二）生鲜类商品存在重量短缺，按短缺部分作相应比例部分退款。</p>
			<p class="text-indent">（三）生鲜类商品存在上述情形，且情节严重的，将视商品实际情形作出退货退款或退款处理。</p>
			<p class="text-indent">情节严重系指：</p>
			<p class="text-indent">（1）商品大小不符、重量短缺及腐烂三类情形两两并存的；</p>
			<p class="text-indent">（2）商品存在大小不符或腐烂情形的，其数量超过商品总量百分之三十的；</p>
			<p class="text-indent">（3）卖家店铺内大量存在上述商品责任情形的。</p>
			<p class="text-indent">除本条第三款，对本条内容买卖双方另有约定的从其约定。</p>
			<p class="text-indent">第五十七条 依本规范作出退货退款处置的，相关运费及商品相应风险由卖家承担。若商品已不适宜退货，则作退款处理，货物由卖家自行联系买家协商处置。</p>
			<p class="text-indent">第五十八条【特殊举证时效】活体类商品买家主张死亡的，需在收到商品当天24点前拍照并上传举证；非活体类商品买家主张变质、腐烂情形的，需在收到商品之时起24小时内拍照并上传举证。若该商品的物理性状极易受时间、环境等客观因素影响而发生质变，买家逾期举证的，不予采信。
			收到商品的时间以快递公司系统内记录签收时间为准。
			买卖双方对举证时间、方式另有约定的，从其约定。</p>

			<h4>第七节 虚拟商品争议处理规范</h4>
			<p class="text-indent">第五十九条 本规范所称的虚拟商品指应属一级类目为“网络游戏点卡”、“网游装备/游戏币/帐号/代练”、“腾讯QQ”、“手机号码/套餐/增值业务”、“移动/联通/电信充值中心”、“网店/网络服务/软件”、“电影/演出/体育赛事”、“购物提货券/蛋糕面包”、“网络店铺代金/优惠券”、“休闲娱乐”、“超市卡/商场购物卡” 、“餐饮美食”、“教育培训”、“婚庆/摄影/摄像服务”下的商品。</p>
			<p class="text-indent">第六十条 卖家应当按照订单约定的数量向买家交付虚拟商品，并确保其交付至买家的虚拟商品符合约定的使用目的和期限。</p>
			<p class="text-indent">第六十一条 因虚拟商品产生的争议，买卖双方应当按照的要求提供相关官方网站的查询记录或者相关单位的书面证明进行举证。</p>
			<p class="text-indent">第六十二条 虚拟商品（除游戏代练、网站建设）交付买家前的商品风险由卖家承担；交付买家后，将根据双方举证并结合实际情况作出判定。</p>
			<p class="text-indent">第六十三条 游戏代练、网站建设类交易，卖家承诺或买卖双方约定的商品交付时间届满前，买家无正当理由要求退款的，需与卖家协商一致。</p>

			卖家存在延迟交付情形的，在买家催告要求交付或申请退款的当日内仍无法交付的，交易支持退款买家；在买家催告要求交付或申请退款的当日内能或已履行交付义务的，卖家按商品实际成交金额的5%（赔付金额最低一元，最高三十元以内）予以赔付买家后，剩余订单金额打款卖家处理，卖家依据规则处置，订单作打款卖家处理。
			<p class="text-indent">第六十四条 以“自动发货”方式交易的虚拟商品，买家未在系统内点击“查看卡密”且在交易完成前（售中）申请退款的，交易做退款处理；在交易完成后（售后），因买家自身原因要求退款的，需与卖家协商一致。</p>
			<p class="text-indent">第六十五条 向买家交付的虚拟商品数量不足或无法达到约定使用期限的，按实际交付比例部分或实际可使用期限做相应比例部分退款。
			虚拟商品存在上述情形，且情节严重的，交易做退款处理。</p>
			<p class="text-indent">情节严重系指：</p>
			<p class="text-indent">（1）交付数量不足、无法达到约定使用期限两类情形并存的；</p>
			<p class="text-indent">（2）卖家店铺内大量存在上述商品责任情形的。向买家交付的虚拟商品多于约定数量的，卖家如需取回商品，应当自行联系买家解决。</p>
			<p class="text-indent">第六十六条 向买家交付的虚拟商品无法达到约定使用目的或者交付错误的，交易做退款处理。</p>
			<p class="text-indent">第六十七条 符合本规范退款情形的，商品由卖家自行联系买家协商处置。</p>
			
			<h4>第九节 大件类家具争议处理规范</h4>
			<p class="text-indent">第六十八条 本规范所称的大件类家具是指商品所在一级类目为天猫及网“住宅家具”和“商业/办公家具”下的商品。
			<p class="text-indent">第八十条大件类家具卖家发货后，买家可事先准备查验或搬运商品所需相关工（载）具便于当场检视商品；
			客观上当场验视商品条件有所欠缺的（如物流点自提、送货至楼下等），应当在签收商品后及时查验，若存在表面不一致的问题，可于48小时内提出主张并提供相关有效凭证（如图片、完整的拆包视频等），依据情形合理选择要求卖家提供维修、补寄、换货、退货等服务，若卖家无正当理由怠于或拒绝履行该等服务的，交易支持退货退款。双方另有约定的从其约定。
			48小时的界定：自签收商品后次日零时起计48小时内。</p>
			<p class="text-indent">第八十一条大件类家具发生退货退款事项的，交易依情形作下列处置，双方另有约定的从其约定：</p>
			<p class="text-indent">（一）非买家原因：</p>
			<p class="text-indent">1、交易订单系卖家提供送货上门并安装服务的，由卖家联系物流上门至买家处拆卸及拉回商品，且不得向买家以任何名义收取任何相关费用，相应退货风险由卖家承担。
			卖家无正当理由怠于或拒绝上门拆卸并拉回商品的，交易支持退款买家，货物由卖家自行联系买家协调处理。</p>
			<p class="text-indent">2、交易订单非系卖家提供送货上门并安装服务的，卖家能够联系物流至买家处上门拆卸及拉回商品的，相关处置依照上述第（一）条第1款。卖家无法做到上门拆卸及拉回商品的，由买家办理退货事宜，买家因退货产生的必要费用由卖家承担。卖家对该必要费用产生争议的，应当先行签收商品，将结合实际情形对该等费用的承担作出处理。</p>
			<p class="text-indent">（二）买家原因：</p>
			<p class="text-indent">由买家办理退货事宜并承担退货产生的必要费用。
			必要费用：系指包括但不限于商品拆卸、打包、加固、搬运等相关费用以及退货运费，该等费用以承运物流出具的相关票据上载明的金额为准。</p>


			<h4>第十节 服务类交易争议处理规范</h4>
			<p class="text-indent">第八十二条本规范所称的服务类交易，系指由卖家向买家提供服务或买家委托卖家处理对应事务的交易。定制、代购、虚拟类交易参照《争议处理规则》对应章节规范执行，对应章节规范中未予明确但符合本规范调整范围的则参照本规范内容执行。</p>
			<p class="text-indent">第八十三条服务订单交付时间届满前，卖家已产生实质服务行为的，或卖家依约已完成服务的，买家要求退款或拒绝接受服务的，需与卖家协商一致。</p>
			<p class="text-indent">第八十四条卖家未按约定履行服务订单的，依据订单履行的相关情形，分别作如下处置，双方另有约定的从其约定：</p>
			<p class="text-indent">（一）卖家无正当理由拒绝履行服务订单，或部分履行服务订单，但已履行部分不可独立使用或虽可独立使用但和未履行部分分离后价值显受贬损的，交易做退款处理。</p>
			<p class="text-indent">（二）卖家部分履行服务订单，已履行部分可独立使用且和未履部分分离后价值不受贬损的，交易按未履行部分比例做相应退款处理。</p>
			<p class="text-indent">（三）卖家存在延迟履行服务订单的：</p>
			<p class="text-indent">卖家在买家催告要求交付服务成果或申请退款之时起24小时内，卖家仍无法交付服务成果的，交易支持退款买家；在买家催告要求交付服务成果或申请退款之时起24小时内，卖家能够交付服务成果的，网卖家按订单实际成交金额的5%（赔付金额最低一元，最高三十元）予以赔付买家后，剩余订单金额打款卖家处理，天猫卖家依据天猫规则处置，订单做打款卖家处理。</p>
			<p class="text-indent">（四）卖家的延迟履行行为致使买家合同目的无法实现的，交易将视服务内容进行退货退款或直接退款处理。合同目的实现与否，将基于服务内容，并以普通大众的认知水准及日常经验进行判断。</p>
			<p class="text-indent">（五）
			卖家在提供服务过程中，非因买家原因而自行加价的，买家可以拒绝接受服务，交易支持退款买家。买家已接受服务的，可要求卖家退还差价。
			“24小时内”的特殊情形：服务的交付，依据其性质需经物流寄递的，卖家需在该时限内将服务成果交寄物流（以物流官网所载明的揽收记录时间为准）。</p>
			<p class="text-indent">第八十五条
			卖家服务过程中，若基于服务履行之必要，需要买家配合提供协助的，或对买家的配合行为存在时限要求的，卖家应当在商品详情页予以显著明示或双方事先约定一致。否则买家有权拒绝并由卖家承担交易不利后果。
			卖家向买家提出配合请求的，买家应在合理期限内予以配合。若买家无正当理由怠于或拒绝提供必要的配合，导致卖家服务成本增加、服务履行期限延迟、服务成果无法实现等不利后果的，应由买家承担不利后果，但卖家仍应及时中止履行服务并采取合理措施防止服务成本的扩大。</p>
			<p class="text-indent">在上述买家责任情形下，相关交易争议作如下处置：</p>
			<p class="text-indent">（一）若服务成果完全无法实现，但卖家确已产生服务成本的，买家在承担相应的服务成本后，交易作退款处理。</p>
			<p class="text-indent">（二）若服务成果已部分实现，则综合评估该部分服务成果的价值比例，并结合订单金额酌情计算其对应价款，交易作部分打款、部分退款处理。</p>
			<p class="text-indent">（三）
			若服务成果已部分实现，同时，剩余未实现的服务成果也已产生服务成本。则分别依照上述第1、2 项处理方法，计算应支付的价款与应承担的服务成本。</p>
			<p class="text-indent">“显著”的界定：</p>
			<p class="text-indent">明示的内容应当位于信息展示界面的“商品（宝贝）详情”版块中，居该版块靠前或居中位置，字体大于该版块其他编辑字体并加粗。</p>
			<p class="text-indent">“合理期限”：系指卖家通过旺旺或买家应当知晓的其他方式（如手机短信）向买家发出通知要求配合之时的次日起48小时内。</p>
			<p class="text-indent">“服务成本”：系指卖家在提供服务的过程中产生的必要费用支出，具体将由根据卖家举证，以普通大众的认知水准及日常经验进行判断，但最高不超过订单实际成交金额。</p>
			<p class="text-indent">第八十六条
			维修服务类交易，卖家应当自签收买家寄修物品次日起30天内完成修理并交付物流（以物流官网所载明的揽收记录时间为准）回寄，回寄的邮费由买家承担，双方另有约定的从其约定。
			维修更换零配件后，被更换的故障配件由双方自行协商处置，不再受理。</p>
			<p class="text-indent">第八十七条
			卖家交付服务成果的同时需要返还买家事先提供用于该服务的物品的，若卖家拒绝返还或返还途中非因买家原因导致物品损毁或灭失的，依据该物品价值及贬损程度，卖家需向买家进行赔偿，若双方事先未有约定，赔偿金额由根据买家提供的物品价值有效凭证或参照当前市场均价予以酌定。对于客观上难以估值的物品，将应买家要求披露卖家信息，以协助买家通过司法途径向卖家进行主张。
			上述情形下，若服务成果已交付且不影响买家合同目的实现的，交易作打款处理，反之，交易作退款处理。合同目的实现与否的判断同本规范第八十四条第（四）项所述判断方式一致。</p>
			<p class="text-indent">第八十八条
			语言翻译类服务，卖家应当确保所交付译件的正确性，依据翻译出错情形，交易作如下处置，双方另有约定的从其约定：</p>
			<p class="text-indent">1、   译件出现1-3处错误的，交易退款20%。</p>
			<p class="text-indent">2、   译件出现4-5处错误的，交易退款50%。</p>
			<p class="text-indent">3、   译件出现错误超过5处的，交易退款处理。</p>
			<p class="text-indent">“错误”系指：译件中存在错译、漏译情形，重复性错误按一处计算。</p>
			<p class="text-indent">第八十九条 买卖双方通过线下完成服务成果交付的，卖家需对交付服务的凭证予以留存并妥善保管，否则由卖家承担交易不利后果。</p>
			<p class="text-indent">第九十条 特别说明：因系普通的第三方交易平台，并非专业的司法机构或鉴定机构：</p>
			<p class="text-indent">1、在对服务成本、损失或价款等给付内容的进行酌定并实施款项分割前，买卖任一方均可向提出中止的争议处理服务，并通过司法途径向对方发起诉讼。</p>
			<p class="text-indent">2、若已经作出服务成本、损失或价款等给付内容的酌定并已实施款项分割的，并不影响双方通过司法途径继续向对方提出主张。</p>
			<p class="text-indent">第九十一条本规范未特别列明的事宜，依照《争议处理规则》执行</p>
          </div>
        </dd>
